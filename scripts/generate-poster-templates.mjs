async function main() {
  const filenames = (await readdir(sourceDir))
    .filter((filename) => filename.endsWith(".css"))
    .sort();

  await mkdir(generatedDir, { recursive: true });
  await writeFile(path.join(generatedDir, "base.css"), baseCss);

  for (const filename of filenames) {
    const templateId = path.basename(filename, ".css");
    const sourcePath = path.join(sourceDir, filename);
    const sourceCss = await readFile(sourcePath, "utf8");
    const generatedCss = generateTemplateCss({
      sourceCss,
      sourcePath,
      templateId,
    });

    await writeFile(path.join(generatedDir, filename), generatedCss);
  }

  console.log(`Generated ${filenames.length} poster template CSS files.`);
}

function generateTemplateCss({ sourceCss, sourcePath, templateId }) {
  const css = stripComments(sourceCss);
  const blocks = parseTopLevelBlocks(css, sourcePath);
  const coveredRanges = [];
  const output = [
    "/* This file is generated by scripts/generate-poster-templates.mjs. */",
  ];
  const rootVariables = [];
  const darkVariables = [];
  const scopedRules = [];

  for (const block of blocks) {
    coveredRanges.push([block.start, block.end]);

    if (block.prelude === ":root") {
      rootVariables.push(...parseCustomProperties(block.body));
      continue;
    }

    if (block.prelude === ".dark") {
      darkVariables.push(...parseCustomProperties(block.body));
      continue;
    }

    if (block.prelude.startsWith("@theme") || block.prelude.startsWith("@layer")) {
      continue;
    }

    if (isTemplateScopedPrelude(block.prelude, templateId)) {
      scopedRules.push(`${block.prelude} {${block.body}}`);
      continue;
    }

    throw new Error(
      `${formatRelative(sourcePath)} contains unsafe selector "${block.prelude}". ` +
        `Use :root, .dark, or selectors scoped to .pm-template--${templateId}.`,
    );
  }

  coveredRanges.push(...findAllowedStatements(css, sourcePath));
  assertNoUnsupportedText(css, coveredRanges, sourcePath);

  if (rootVariables.length > 0) {
    output.push(formatVariableRule(`.pm-template--${templateId}`, rootVariables));
  }

  if (darkVariables.length > 0) {
    output.push(
      formatVariableRule(`.dark .pm-template--${templateId}`, darkVariables),
    );
  }

  output.push(...scopedRules.map((rule) => normalizeRule(rule)));

  return `${output.filter(Boolean).join("\n\n")}\n`;
}

function stripComments(css) {
  return css.replace(/\/\*[\s\S]*?\*\//g, "");
}

function parseTopLevelBlocks(css, sourcePath) {
  const blocks = [];
  let index = 0;

  while (index < css.length) {
    const openIndex = css.indexOf("{", index);

    if (openIndex === -1) {
      break;
    }

    const closeIndex = findMatchingBrace(css, openIndex, sourcePath);
    const preludeStart = findPreludeStart(css, openIndex);
    const prelude = css.slice(preludeStart, openIndex).trim();

    if (prelude.length === 0) {
      throw new Error(`${formatRelative(sourcePath)} contains an empty CSS rule.`);
    }

    blocks.push({
      body: css.slice(openIndex + 1, closeIndex),
      end: closeIndex + 1,
      prelude,
      start: preludeStart,
    });

    index = closeIndex + 1;
  }

  return blocks;
}

function findMatchingBrace(css, openIndex, sourcePath) {
  let depth = 0;

  for (let index = openIndex; index < css.length; index += 1) {
    const char = css[index];

    if (char === "{") {
      depth += 1;
    } else if (char === "}") {
      depth -= 1;

      if (depth === 0) {
        return index;
      }
    }
  }

  throw new Error(`${formatRelative(sourcePath)} contains an unmatched "{".`);
}

function findPreludeStart(css, openIndex) {
  const previousClose = css.lastIndexOf("}", openIndex);
  const previousSemicolon = css.lastIndexOf(";", openIndex);
  return Math.max(previousClose, previousSemicolon) + 1;
}

function parseCustomProperties(body) {
  const variables = [];
  const declarationPattern = /(--[a-zA-Z0-9-_]+)\s*:\s*([^;]+);/g;
  let match;

  while ((match = declarationPattern.exec(body))) {
    variables.push([match[1], match[2].trim()]);
  }

  return variables;
}

function isTemplateScopedPrelude(prelude, templateId) {
  return prelude
    .split(",")
    .map((selector) => selector.trim())
    .every(
      (selector) =>
        selector.startsWith(`.pm-template--${templateId}`) ||
        selector.startsWith(`.dark .pm-template--${templateId}`),
    );
}

function findAllowedStatements(css, sourcePath) {
  const ranges = [];
  const statementPattern = /@(import|custom-variant)\s+[^;]+;/g;
  let match;

  while ((match = statementPattern.exec(css))) {
    const statement = match[0];

    if (
      match[1] === "import" &&
      !/^@import\s+["']tailwindcss["'];$/.test(statement.trim())
    ) {
      throw new Error(
        `${formatRelative(sourcePath)} contains unsupported import "${statement.trim()}".`,
      );
    }

    ranges.push([match.index, match.index + statement.length]);
  }

  return ranges;
}

function assertNoUnsupportedText(css, coveredRanges, sourcePath) {
  const sortedRanges = coveredRanges.sort((left, right) => left[0] - right[0]);
  let cursor = 0;

  for (const [start, end] of sortedRanges) {
    const leftover = css.slice(cursor, start).trim();

    if (leftover.length > 0) {
      throw new Error(
        `${formatRelative(sourcePath)} contains unsupported CSS near "${leftover.slice(
          0,
          80,
        )}".`,
      );
    }

    cursor = Math.max(cursor, end);
  }

  const tail = css.slice(cursor).trim();

  if (tail.length > 0) {
    throw new Error(
      `${formatRelative(sourcePath)} contains unsupported CSS near "${tail.slice(
        0,
        80,
      )}".`,
    );
  }
}

function formatVariableRule(selector, variables) {
  const declarations = [...variables, ...templateVariableMappings]
    .map(([name, value]) => `  ${name}: ${value};`)
    .join("\n");

  return `${selector} {\n${declarations}\n}`;
}

function normalizeRule(rule) {
  return rule.trim().replace(/\n{3,}/g, "\n\n");
}

function formatRelative(filename) {
  return path.relative(rootDir, filename);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
